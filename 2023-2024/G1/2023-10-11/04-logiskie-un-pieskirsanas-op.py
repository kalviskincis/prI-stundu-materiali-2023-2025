# 2. Izveidojiet programmu, kas pārbauda, vai dotais skaitlis ir lielāks par 10 un mazāks par 20. Izmantojiet `>` un `<` operatorus.

c = 15
# Tavs kods šeit
print(c>10)
print(c<20)
print(c>10 and c<20)


# 3. Izveidojiet programmu, kas pārbauda, vai dotais skaitlis ir mazāks vai vienāds ar 10, vai arī lielāks vai vienāds ar 20. Izmantojiet `<=` un `>=` operatorus.

d = 25
# Tavs kods šeit
print(d <= 10)
print(d >= 20)
print(d <= 10 or d >= 20)

# 4. Izveidojiet programmu, kas pārbauda, vai dotais skaitlis ir starp 10 un 20, vai arī starp 30 un 40. Izmantojiet `and` un `or` operatorus.

e = 35
# Tavs kods šeit
print((e>10 and e<20) or (e>30 and e<40))

# 5. Izveidojiet programmu, kas pārbauda, vai dotais skaitlis nav 10. Izmantojiet `!=` operatoru.

f = 10
# Tavs kods šeit
print(f != 10)
